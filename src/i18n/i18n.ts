const initI18n = async () => {
  let savedLanguage = await AsyncStorage.getItem('app_language');
  if (!savedLanguage) {
    savedLanguage = 'ru';
  }

  i18n
    .use(initReactI18next)
    .init({
      resources,
      lng: savedLanguage,
      fallbackLng: 'ru',
      interpolation: {
        escapeValue: false,
      },
    });
};

initI18n();

export default i18n;
