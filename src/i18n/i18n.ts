const initI18n = async () => {
  let savedLanguage = await AsyncStorage.getItem('app_language');
  if (!savedLanguage) {
    savedLanguage = 'ru';
  }

  await i18n
    .use(initReactI18next)
    .init({
      resources,
      lng: savedLanguage,
      fallbackLng: 'ru',
      interpolation: {
        escapeValue: false,
      },
      returnNull: false,
      react: {
        useSuspense: false,
      },
    });
};

initI18n();

